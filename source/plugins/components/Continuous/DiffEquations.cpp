bool DiffEquations::_loadInstance(PersistenceRecord *fields) {
	bool res = ModelComponent::_loadInstance(fields);
	if (res) {
		_precision = fields->loadField("precision", DEFAULT.precision);
		_maxSteps = fields->loadField("maxSteps", DEFAULT.maxSteps);
		_finalTime = fields->loadField("finalTime", DEFAULT.finalTime);

		_equations->clear();
		std::string equationsStr = fields->loadField("equations", "");
		if (!equationsStr.empty()) {
			size_t pos = 0;
			size_t delimPos;
			while ((delimPos = equationsStr.find(";", pos)) != std::string::npos) {
				_equations->insert(equationsStr.substr(pos, delimPos - pos));
				pos = delimPos + 1;
			}
			if (pos < equationsStr.length()) {
				_equations->insert(equationsStr.substr(pos));
			}
		}

		_stateVariableNames->clear();
		std::string stateVarsStr = fields->loadField("stateVariableNames", "");
		if (!stateVarsStr.empty()) {
			size_t pos = 0;
			size_t delimPos;
			while ((delimPos = stateVarsStr.find(";", pos)) != std::string::npos) {
				_stateVariableNames->insert(stateVarsStr.substr(pos, delimPos - pos));
				pos = delimPos + 1;
			}
			if (pos < stateVarsStr.length()) {
				_stateVariableNames->insert(stateVarsStr.substr(pos));
			}
		}

		std::string cppCompilerName = fields->loadField("cppCompiler", "");
		_cppCompiler = nullptr;
		if (!cppCompilerName.empty()) {
			ModelDataDefinition* def = _parentModel->getDataManager()->getDataDefinition(
					Util::TypeOf<CppCompiler>(), cppCompilerName);
			_cppCompiler = dynamic_cast<CppCompiler*>(def);
		}
	}
	return res;
}

void DiffEquations::_saveInstance(PersistenceRecord *fields, bool saveDefaultValues) {
	ModelComponent::_saveInstance(fields, saveDefaultValues);

	std::string equationsStr = "";
	for (unsigned int i = 0; i < _equations->size(); i++) {
		equationsStr += _equations->getAtRank(i);
		if (i < _equations->size() - 1) {
			equationsStr += ";";
		}
	}
	fields->saveField("equations", equationsStr, "", saveDefaultValues);

	std::string stateVarsStr = "";
	for (unsigned int i = 0; i < _stateVariableNames->size(); i++) {
		stateVarsStr += _stateVariableNames->getAtRank(i);
		if (i < _stateVariableNames->size() - 1) {
			stateVarsStr += ";";
		}
	}
	fields->saveField("stateVariableNames", stateVarsStr, "", saveDefaultValues);
	fields->saveField("precision", _precision, DEFAULT.precision, saveDefaultValues);
	fields->saveField("maxSteps", _maxSteps, DEFAULT.maxSteps, saveDefaultValues);
	fields->saveField("finalTime", _finalTime, DEFAULT.finalTime, saveDefaultValues);
	fields->saveField("cppCompiler",
	                  _cppCompiler != nullptr ? _cppCompiler->getName() : std::string(""),
	                  std::string(""),
	                  saveDefaultValues);
}

void DiffEquations::_onDispatchEvent(Entity* entity, unsigned int inputPortNumber) {
	(void)inputPortNumber;
	traceSimulation(this, "Invoking cpp user code for dispatchEvent");
	const unsigned int n = _equations->size();
	if (n == 0) {
		this->_parentModel->sendEntityToComponent(entity, this->getConnectionManager()->getFrontConnection());
		return;
	}
	std::vector<double> stateVars(n, 0.0);
	for (unsigned int i = 0; i < n; i++) {
		if (_stateVariableNames->size() > i) {
			Variable* stateVar = dynamic_cast<Variable*>(_parentModel->getDataManager()->getDataDefinition(
			        Util::TypeOf<Variable>(), _stateVariableNames->getAtRank(i)));
			if (stateVar != nullptr) {
				stateVars[i] = stateVar->getValue();
			}
		}
	}
	const double t0 = 0.0;
	const double tTarget = _parentModel->getSimulation()->getSimulatedTime();
	double step = (_maxSteps > 0.0) ? ((tTarget - t0) / _maxSteps) : 0.01;
	if (step <= 0.0) {
		step = 0.01;
	}
	try {
		if (dispatchEvent_SharedLibHandler != nullptr) {
			dispatchEvent_SharedLibHandler(stateVars.data(), static_cast<int>(n), t0, tTarget, step);
			_valuesOnFinalTime->clear();
			for (unsigned int i = 0; i < n; i++) {
				_valuesOnFinalTime->insert(stateVars[i]);
				if (_stateVariableNames->size() > i) {
					Variable* stateVar = dynamic_cast<Variable*>(_parentModel->getDataManager()->getDataDefinition(
					        Util::TypeOf<Variable>(), _stateVariableNames->getAtRank(i)));
					if (stateVar != nullptr) {
						stateVar->setValue(stateVars[i]);
					}
				}
			}
		}
	} catch (const std::exception& e) {
		(void)e;
	}
	this->_parentModel->sendEntityToComponent(entity, this->getConnectionManager()->getFrontConnection());
}


