bool isIdentifierChar(char c) {
	return std::isalnum(static_cast<unsigned char>(c)) != 0 || c == '_';
}

std::string substituteStateVariablesInExpression(const std::string& expression,
                                                 const std::vector<std::string>& stateVariableNames) {
	std::string result = expression;
	std::vector<size_t> indices(stateVariableNames.size());
	for (size_t i = 0; i < indices.size(); ++i) {
		indices[i] = i;
	}
	// sort by descending name length so a longer name like "vel" is replaced before a
	// shorter prefix like "v", preventing partial substitutions inside longer identifiers
	std::sort(indices.begin(), indices.end(), [&](size_t a, size_t b) {
		return stateVariableNames[a].length() > stateVariableNames[b].length();
	});
	for (size_t idx : indices) {
		const std::string& name = stateVariableNames[idx];
		if (name.empty()) {
			continue;
		}
		const std::string replacement = "y[" + std::to_string(idx) + "]";
		size_t pos = 0;
		while (pos < result.size()) {
			const size_t found = result.find(name, pos);
			if (found == std::string::npos) {
				break;
			}
			const bool leftOk = (found == 0) || !isIdentifierChar(result[found - 1]);
			const bool rightOk = (found + name.size() >= result.size())
			                     || !isIdentifierChar(result[found + name.size()]);
			if (leftOk && rightOk) {
				result.replace(found, name.size(), replacement);
				pos = found + replacement.size();
			} else {
				pos = found + 1;
			}
		}
	}
	return result;
}

std::vector<std::string> resolveStateVariableNames(List<std::string>* stateNames,
                                                   unsigned int equationCount) {
	std::vector<std::string> names;
	if (stateNames != nullptr && stateNames->size() == equationCount) {
		for (unsigned int i = 0; i < equationCount; i++) {
			names.push_back(stateNames->getAtRank(i));
		}
	}
	if (names.empty()) {
		for (unsigned int i = 0; i < equationCount; i++) {
			names.push_back("x" + std::to_string(i));
		}
	}
	return names;
}

std::string buildDiffEquationsSource(const std::string& componentName,
                                     const std::vector<std::string>& stateVariableNames,
                                     List<std::string>* equations,
                                     double maxSteps) {
	const unsigned int n = equations->size();
	std::string sourceCode =
	        "//\n"
	        "// File automatically generated by GenESyS DiffEquations plugin\n"
	        "// Component: " + componentName + "\n"
	        "//\n"
	        "#include <cmath>\n"
	        "#include <cstring>\n"
	        "\n"
	        "static const int N = " + std::to_string(n) + ";\n"
	        "static const int MAX_STEPS = " + std::to_string(static_cast<int>(maxSteps)) + ";\n"
	        "\n"
	        "static void derivatives(double t, const double* y, double* dydt) {\n"
	        "\t(void)t;\n";
	for (unsigned int i = 0; i < n; i++) {
		const std::string substituted =
		        substituteStateVariablesInExpression(equations->getAtRank(i), stateVariableNames);
		sourceCode += "\tdydt[" + std::to_string(i) + "] = " + substituted + ";\n";
	}
	sourceCode +=
	        "}\n"
	        "\n"
	        "static void rk4_step(double t, double h, double* y) {\n"
	        "\tdouble k1[N], k2[N], k3[N], k4[N], ytmp[N], f[N];\n"
	        "\tderivatives(t, y, f);\n"
	        "\tfor (int i = 0; i < N; ++i) {\n"
	        "\t\tk1[i] = h * f[i];\n"
	        "\t}\n"
	        "\tfor (int i = 0; i < N; ++i) {\n"
	        "\t\tytmp[i] = y[i] + 0.5 * k1[i];\n"
	        "\t}\n"
	        "\tderivatives(t + 0.5 * h, ytmp, f);\n"
	        "\tfor (int i = 0; i < N; ++i) {\n"
	        "\t\tk2[i] = h * f[i];\n"
	        "\t}\n"
	        "\tfor (int i = 0; i < N; ++i) {\n"
	        "\t\tytmp[i] = y[i] + 0.5 * k2[i];\n"
	        "\t}\n"
	        "\tderivatives(t + 0.5 * h, ytmp, f);\n"
	        "\tfor (int i = 0; i < N; ++i) {\n"
	        "\t\tk3[i] = h * f[i];\n"
	        "\t}\n"
	        "\tfor (int i = 0; i < N; ++i) {\n"
	        "\t\tytmp[i] = y[i] + k3[i];\n"
	        "\t}\n"
	        "\tderivatives(t + h, ytmp, f);\n"
	        "\tfor (int i = 0; i < N; ++i) {\n"
	        "\t\tk4[i] = h * f[i];\n"
	        "\t}\n"
	        "\tfor (int i = 0; i < N; ++i) {\n"
	        "\t\ty[i] += (k1[i] + 2.0 * k2[i] + 2.0 * k3[i] + k4[i]) / 6.0;\n"
	        "\t}\n"
	        "}\n"
	        "\n"
	        "extern \"C\" void onDispatchEvent(double* stateVars, int n, double t0,\n"
	        "                                double tTarget, double step) {\n"
	        "\tif (stateVars == nullptr || n != N || step <= 0.0 || tTarget < t0) {\n"
	        "\t\treturn;\n"
	        "\t}\n"
	        "\tdouble y[N];\n"
	        "\tstd::memcpy(y, stateVars, static_cast<size_t>(N) * sizeof(double));\n"
	        "\tdouble t = t0;\n"
	        "\tint steps = 0;\n"
	        "\twhile (t + step <= tTarget + 1e-15 && steps < MAX_STEPS) {\n"
	        "\t\trk4_step(t, step, y);\n"
	        "\t\tt += step;\n"
	        "\t\t++steps;\n"
	        "\t}\n"
	        "\tif (t < tTarget - 1e-15 && steps < MAX_STEPS) {\n"
	        "\t\trk4_step(t, tTarget - t, y);\n"
	        "\t\tt = tTarget;\n"
	        "\t}\n"
	        "\tstd::memcpy(stateVars, y, static_cast<size_t>(N) * sizeof(double));\n"
	        "}\n";
	return sourceCode;
}

